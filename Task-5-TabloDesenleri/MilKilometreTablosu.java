public class MilKilometreTablosu {
    public static void main(String[] args) {
        double katsayi = 1.609; // 1 mil = 1.609 km

        System.out.println("Mil     Kilometre");
        System.out.println("------------------");

        System.out.println("1       " + (1 * katsayi));
        System.out.println("5       " + (5 * katsayi));
        System.out.println("10      " + (10 * katsayi));
        System.out.println("20      " + (20 * katsayi));
        System.out.println("50      " + (50 * katsayi));
    }
}
